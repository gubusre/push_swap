#include "pushswap.h"

void	bubble_sort(t_list *d)
{
	int	i;
	int	c;
	int	j;

	i = 0;
	c = 0;
	j = d->argc;
	while (c < j)
	{
		while (i < j - 1)
		{
			if (d->s_s[i] > d->s_s[i + 1])
				ft_swap(&d->s_s[i], &d->s_s[i + 1]);
			i++;
		}
		i = 0;
		c++;
	}
}
