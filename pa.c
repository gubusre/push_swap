#include "pushswap.h"

void	pa(t_list *d)
{
	int	i;

	i = d->isa;
	d->isb = d->isb - 1;
	d->isa = d->isa + 1;
	while (i > 0)
	{
		ft_swap(&d->s_a[i], &d->s_a[i - 1]);
		i--;
	}
	d->s_a[0] = d->s_b[0];
	i = 0;
	while (i <= d->isb)
	{
		d->s_b[i] = d->s_b[i + 1];
		i++;
	}
	write(1, "pa\n", 3);
}
