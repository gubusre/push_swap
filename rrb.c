#include "pushswap.h"

void	rrb(t_list *d)
{
	int	i;
	int	j;

	i = d->isb - 1;
	j = 0;
	while (i - 1 >= 0)
	{
		ft_swap(&d->s_b[i], &d->s_b[i - 1]);
		i--;
	}
	write(1, "rrb\n", 4);
}
