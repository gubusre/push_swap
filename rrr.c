#include "pushswap.h"

void	rrr(t_list *d)
{
	int	i;

	i = d->isa - 1;
	while (i > 0)
	{
		ft_swap(&d->s_a[i], &d->s_a[i - 1]);
		i--;
	}
	i = d->isb - 1;
	while (i - 1 >= 0)
	{
		ft_swap(&d->s_b[i], &d->s_b[i - 1]);
		i--;
	}
	write(1, "rrr\n", 4);
}
