#include "pushswap.h"

void	rb(t_list *d)
{
	int	i;

	i = 0;
	while (i < d->isb - 1)
	{
		ft_swap(&d->s_b[i], &d->s_b[i + 1]);
		i++;
	}
	write(1, "rb\n", 3);
}
