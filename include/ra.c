#include "pushswap.h"

void	ra(t_list *d)
{
	int	i;

	i = 0;
	while (i < (d->isa - 1))
	{
		ft_swap(&d->s_a[i], &d->s_a[i + 1]);
		i++;
	}
	write(1, "ra\n", 3);
}
