#include "pushswap.h"

void	ft_index(t_list *d)
{
	int	j;
	int	i;

	j = 0;
	i = 0;
	while (j < d->argc)
	{
		if (d->s_a[j] != d->s_s[i])
			i++;
		else
		{
			d->s_a[j] = i;
			j++;
			i = 0;
		}
	}
}
