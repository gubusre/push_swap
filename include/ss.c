#include "pushswap.h"

void	ss(t_list *d)
{
	int	tmp;
	int	temp;

	tmp = d->s_a[0];
	temp = d->s_b[0];
	d->s_a[0] = d->s_a[1];
	d->s_b[0] = d->s_b[1];
	d->s_a[1] = tmp;
	d->s_b[1] = temp;
	write(1, "ss\n", 3);
}
