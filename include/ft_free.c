#include "pushswap.h"

void	ft_free(t_list *d)
{
	if (d->s_a)
	{
		free(d->s_a);
		d->s_a = NULL;
	}
	if (d->s_b)
	{
		free(d->s_b);
		d->s_b = NULL;
	}
	if (d->s_s)
	{
		free(d->s_s);
		d->s_s = NULL;
	}
	if (d)
	{
		free(d);
		d = NULL;
	}
	exit(0);
}
