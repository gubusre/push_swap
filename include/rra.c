#include "pushswap.h"

void	rra(t_list *d)
{
	int	i;

	i = d->isa - 1;
	while (i > 0)
	{
		ft_swap(&d->s_a[i], &d->s_a[i - 1]);
		i--;
	}
	write(1, "rra\n", 4);
}
