#include "pushswap.h"

void	ft_efree(t_list *d)
{
	if (d->s_a)
	{
		free(d->s_a);
	}
	if (d->s_b)
	{
		free(d->s_b);
	}
	if (d->s_s)
	{
		free(d->s_s);
	}
	if (d)
	{
		free(d);
	}
	write(2, "Error\n", 6);
	exit(0);
}
