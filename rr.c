#include "pushswap.h"

void	rr(t_list *d)
{
	int	i;

	i = 0;
	while (i + 1 < d->isa)
	{
		ft_swap(&d->s_a[i], &d->s_a[i + 1]);
		i++;
	}
	i = 0;
	while (i + 1 < d->isb)
	{
		ft_swap(&d->s_b[i], &d->s_b[i + 1]);
		i++;
	}
	write(1, "rr\n", 3);
}
